package com.project.example.inheritance;

public class A {
	public void print_test_A() {
		System.out.println("Testing class A...");
	}
}

class B extends A {
	public void print_test_B() {
		System.out.println("Testing class B...");
	}
}

class C extends A {
	public void print_test_C() {
		System.out.println("Testing class C...");
	}
}

class D extends A {
	public void print_test_D() {
		System.out.println("Testing Class D...");
	}
}